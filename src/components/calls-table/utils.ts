export const getColorByCallTypeId = (value?: string) => {
  switch (value) {
    case '1':
      return '#22c55e';
    case '2':
      return '#f43f5e';
    case '3':
      return '#fde047';
    case '4':
      return '#3b82f6';
    case '5':
      return '#8b5cf6';
    case '6':
      return '#f97316';
  }
};
